import com.qianfeng.openapi.web.mq.RabbitMQOutput;
import org.mybatis.spring.annotation.MapperScan;
import org.springframework.boot.SpringApplication;
import org.springframework.boot.autoconfigure.SpringBootApplication;
import org.springframework.boot.web.servlet.ServletComponentScan;
import org.springframework.cloud.client.discovery.EnableDiscoveryClient;
import org.springframework.cloud.openfeign.EnableFeignClients;
import org.springframework.cloud.stream.annotation.EnableBinding;

/**
 * Created by jackiechan on 19-12-27 下午4:48
 *
 * @Author jackiechan
 */
@SpringBootApplication
@EnableDiscoveryClient
@EnableFeignClients
@MapperScan("com.qianfeng.openapi.web.mapper")
@ServletComponentScan("com.qianfeng.openapi.web.listener")//扫描监听器
@EnableBinding({RabbitMQOutput.class})//绑定接口
public class WebMasterStartApp {
    public static void main (String[] args){
        SpringApplication.run(WebMasterStartApp.class,args);
    }
}
