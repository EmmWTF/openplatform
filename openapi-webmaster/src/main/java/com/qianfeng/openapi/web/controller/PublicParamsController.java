import com.qianfeng.openapi.web.event.EventType;
import com.qianfeng.openapi.web.mapper.PublicParmasMapper;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.context.ApplicationContext;
import org.springframework.web.bind.annotation.PathVariable;
import org.springframework.web.bind.annotation.PostMapping;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RestController;

/**
 * Created by jackiechan on 19-12-28 下午3:39
 *
 * @Author jackiechan
 */
@RestController
@RequestMapping("/params")
public class PublicParamsController {
    @Autowired
    private PublicParmasMapper publicParmasMapper;

    @Autowired
    private ApplicationContext context;

    @PostMapping("/params/{paramname}/{status}")
    public void addParam(@PathVariable  String paramname,@PathVariable  int status) {
        publicParmasMapper.addParam(paramname, status);//添加到数据库中
        //同步数据到redis中
        context.publishEvent(EventType.PUBLICPARMAS);

    }
}
